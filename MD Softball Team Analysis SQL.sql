SELECT TOP 5 o.oppName AS 'Opponent', COUNT(*) as 'Maryland SoftBall Wins'
FROM [CurveBallConsultants.Match] m
JOIN [CurveBallConsultants.Opponent] o ON m.oppId = o.oppId
WHERE m.winLoss = 'W'
GROUP BY o.oppName
ORDER BY 'Maryland SoftBall Wins' DESC;

-- 2. What are the top five teams against whom Maryland Softball lost the most matches in the given range of years? --
SELECT TOP 5 o.oppName AS 'Opponent', COUNT(*) as 'Maryland SoftBall Losses'
FROM [CurveBallConsultants.Match] m
JOIN [CurveBallConsultants.Opponent] o ON m.oppId = o.oppId
WHERE m.winLoss = 'L'
GROUP BY o.oppName
ORDER BY 'Maryland SoftBall Losses' DESC;

-- 3. Which are the top 5 locations that have most matches in the given range of years? --
SELECT TOP 5 locCity AS 'City', locState AS 'State', COUNT(*) AS 'Matches Hosted'
FROM [CurveBallConsultants.Match] m
JOIN [CurveBallConsultants.Location] l ON m.locId = l.locId
WHERE m.ssnYear BETWEEN '1999' AND '2022'
GROUP BY l.locCity, l.locState
ORDER BY COUNT(*) DESC;

-- 4. What is Maryland's all-time win percentage for each opponent against whom more than 5 matches have been played?
