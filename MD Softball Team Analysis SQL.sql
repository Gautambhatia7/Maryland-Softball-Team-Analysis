FROM [CurveBallConsultants.Opponent] o
JOIN [CurveBallConsultants.Match] m ON o.oppId = m.oppId
GROUP BY o.oppName
HAVING COUNT(*) > 5
ORDER BY 'Win Percentage (%)' DESC, o.oppName DESC;

